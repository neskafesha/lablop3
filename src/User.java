public class User {
    public static String Name;
    public static String Password;

    public User() {
    }

    public String getUser() {
        return Name;
    }

    public void setUser(String user) {
        this.Name = user;
    }

    public String getPassword() {
        return Password;
    }

    public void setPassword(String password) {
        this.Password = password;
    }
}
